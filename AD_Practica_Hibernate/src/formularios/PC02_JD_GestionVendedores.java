    private void txtDNIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDNIActionPerformed
        
        boolean correcto=MetodosComunes.comprobarDNI(txtDNI.getText());
        
        if(correcto){
            
            boolean existe=conexion.ExisteValor(txtDNI.getText().toUpperCase(), "DNI", "ExVendedores");
            
            txtDNI.setEnabled(false);
            
            activar();
            
            if(existe){
                
                codigoOperacion=UPDATE;
                
                rellenar(txtDNI.getText());
                
            }else{
             
                codigoOperacion=INSERT;
                
            }
            
            
        }else{
            JOptionPane.showMessageDialog(this, "DNI incorrecto");
            txtDNI.requestFocus();
        }
        
        
    }//GEN-LAST:event_txtDNIActionPerformed

