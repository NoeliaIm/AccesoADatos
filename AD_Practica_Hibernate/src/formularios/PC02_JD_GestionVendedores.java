    public void nuevoVendedor(){
        
        txtDNI.setText("");
        txtNombreVend.setText("");
        txtDireccion.setText("");
        txtTelefono.setText("");
        comboZonas.setSelectedIndex(0);
        
        txtDNI.setEnabled(true);
        txtNombreVend.setEnabled(false);
        txtDireccion.setEnabled(false);
        txtTelefono.setEnabled(false);
        comboZonas.setEnabled(false);
        btnNuevo.setEnabled(false);
        btnGrabar.setEnabled(false);
        btnEliminar.setEnabled(false);
        idVendedorActual=-1;
        
    }
    
    public void activar(){
        txtDNI.setEnabled(false);
        txtNombreVend.setEnabled(true);
        txtDireccion.setEnabled(true);
        txtTelefono.setEnabled(true);
        comboZonas.setEnabled(true);
        btnNuevo.setEnabled(true);
        btnGrabar.setEnabled(true);
        btnEliminar.setEnabled(true);
    }
   
    public void rellenar(String DNI){
        
        conexion.abrirSesion();
        ExVendedores vend=(ExVendedores)conexion.getSesion().createQuery("from ExVendedores where DNI='"+DNI.toUpperCase()+"'").uniqueResult();
        idVendedorActual=vend.getIdvendedor();

        txtNombreVend.setText(vend.getNombreVend());
        txtDireccion.setText(vend.getDireccion());
        txtTelefono.setText(String.valueOf(vend.getTelefono()));
        MetodosComunes.asignarItemCmb2Columnas(comboZonas, vend.getExZonas().getIdzona());
        txtDNI.setText(DNI);
        conexion.cerrarSesion();
    }
    
    public void cambioFlechas(){
        
        conexion.abrirSesion();
        
        Query consulta=conexion.getSesion().createQuery(CONSULTA_VEND);
        
        ExVendedores vend=(ExVendedores)consulta.list().get(numRegistro-1);
        conexion.cerrarSesion();
        
        rellenar(vend.getDni());
        
        activar();
        
        codigoOperacion=UPDATE;
        
    }
    
    
