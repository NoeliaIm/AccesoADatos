    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       
        int eleccion=JOptionPane.showConfirmDialog(this, "Confirmacion", "¿Estas seguro de querer borrar el vendedor?",JOptionPane.YES_NO_OPTION);
        
        if(eleccion==JOptionPane.YES_OPTION){
            
            conexion.abrirSesionYTransaccion();
            
            ExVendedores vend=(ExVendedores)conexion.getSesion().load(ExVendedores.class, idVendedorActual);
            
            vend.setEliminado(true);
            
            conexion.update(vend);
            
            conexion.cerrarSesionYTransaccion();
            
            nuevoVendedor();
            
            maximo-=1;
            
            JOptionPane.showMessageDialog(this, "Vendedor eliminado");
            
            
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

