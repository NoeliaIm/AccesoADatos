    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed

        String error="";

        Object[][] matriz={
                            {txtNombreVend, txtDireccion, txtTelefono},
                            {"- Escribe un nombre\n", "- Escribe una direccion\n", "- Escribe un telefono\n"}
        };
        
        error+=MetodosComunes.comprobarVacios(matriz);
        
        if(comboZonas.getSelectedIndex()==0){
           error+="- Elige una zona"; 
        }
        
        if(error.equals("")){
            
            ExVendedores vend;
            
            conexion.abrirSesionYTransaccion();
            
            String nombre=txtNombreVend.getText();
            String DNI=txtDNI.getText().toUpperCase();
            String direccion=txtDireccion.getText();
            int telefono=Integer.parseInt(txtTelefono.getText());
            
            ExZonas zona=(ExZonas)conexion.getSesion().load(ExZonas.class, MetodosComunes.devolverCodigoComboBox(comboZonas));
            
            String mensaje;
            
            if(codigoOperacion==INSERT){
                
                vend=new ExVendedores();
                
                idVendedorActual=conexion.proximoIDDisponible("idvendedor", "ExVendedores");
                
                vend.setIdvendedor(idVendedorActual);
                vend.setNombreVend(nombre);
                vend.setDireccion(direccion);
                vend.setTelefono(telefono);
                vend.setDni(DNI);
                vend.setExZonas(zona);
                vend.setEliminado(false);
                
                conexion.insertar(vend);
                
                mensaje="Vendedor añadido";
                
                maximo+=1;
                
                nuevoVendedor();
                
            }else{
                
                vend=(ExVendedores)conexion.getSesion().load(ExVendedores.class, idVendedorActual);
                
                vend.setNombreVend(nombre);
                vend.setDireccion(direccion);
                vend.setTelefono(telefono);
                vend.setExZonas(zona);
                
                conexion.update(vend);
                
                mensaje="Vendedor Modificado";
            }
            
            conexion.cerrarSesionYTransaccion();
            
            JOptionPane.showMessageDialog(this, mensaje);
            
        }else{
            JOptionPane.showMessageDialog(this, error);
        }
        
    }//GEN-LAST:event_btnGrabarActionPerformed

