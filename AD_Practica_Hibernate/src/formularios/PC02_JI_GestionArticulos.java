    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
        if(modelo.getRowCount()==0){
            
            JOptionPane.showMessageDialog(this, "No hay registros que eliminar");
            
        }else{
            
            int fila=TablaArticulos.getSelectedRow();
            
            if(fila == -1){
                       
                JOptionPane.showMessageDialog(this, "No has selccionado ninguna fila");
         
            }else{
         
                int idarticulo=(int)TablaArticulos.getValueAt(fila, 0);
             
                PC02_JD_Articulos ventana=new PC02_JD_Articulos(idarticulo);
        
                ventana.setVisible(true);

                rellenarTabla(CONSULTA+devolverEstado()+devolverFiltro()+ORDEN);
                
            }
        
        }
        
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        if(modelo.getRowCount()==0){
            
            JOptionPane.showMessageDialog(this, "No hay registros que eliminar");
            
        }else{
            
            int fila=TablaArticulos.getSelectedRow();
            
            if(fila == -1){
                
                JOptionPane.showMessageDialog(this, "No has selccionado ninguna fila");
                
            }else{
                
                int eleccion=JOptionPane.showConfirmDialog(this, "¿Estas seguro de que quieres eliminar el articulo?");
                
                if(eleccion==JOptionPane.YES_OPTION){
                    
                    int idarticulo=(int)TablaArticulos.getValueAt(fila, 0);

                    conexion.abrirSesionYTransaccion();

                    ExArticulos art=(ExArticulos)conexion.getSesion().load(ExArticulos.class, idarticulo);

                    art.setEliminado(true);

                    conexion.update(art);

                    conexion.cerrarSesionYTransaccion();

                    JOptionPane.showMessageDialog(this, "Articulo eliminado");
                    
                }
                
                
                        
            }
            
        }
        
    }//GEN-LAST:event_btnEliminarActionPerformed

