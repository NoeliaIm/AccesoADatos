    public void rellenarTabla(String consulta){

        conexion.abrirSesion();

        MetodosComunes.limpiarTabla(modelo);

        Query q=conexion.getSesion().createQuery(consulta);

        q.setFetchSize(10);

        //Cambiar clase, si es cruzada poner Object[]
        Iterator<ExArticulos> it= q.iterate();
        ExArticulos fila;

        //Pasar el numero de columnas que tenemos
        Object[] values=new Object[COLUMNAS];

        while(it.hasNext()){

            fila = it.next();

            //Añadir metodos del objeto y guardarlos en el array de objetos
            //values[0]=metodo;
            //...
            values[0]=fila.getIdarticulo(); 
            values[1]=fila.getDescrArtic();
            values[2]=fila.getPrecioUnitario();

            //añade al modelo(DefaultTableModel)
            modelo.addRow(values);

   }

        MetodosComunes.ocultarColumnaJTable(TablaArticulos, 0);
        
   conexion.cerrarSesion();
}

