    private void btnAnadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAnadirActionPerformed
        
        
        String error="";
         
        JComboBox[] combos={comboVendedores, comboArticulos, comboZonas};
        String[] mensajes={"vendedores", "articulo", "zonas"};
        for(int i=0;i<combos.length;i++){
            
            if(combos[i].getSelectedIndex()==0){
                error+="- Debes elegir algun valor en "+mensajes[i]+"\n";
            }
            
        }
        
        if(dateFechaVenta.getDate()==null){
            error+="- Debes elegir una fecha\n";
        }
        
        if(txtUnidades.getText().equals("")){
            error+="- Debes elegir una cantidad\n";
        }
        if(error.equals("")){
            
            conexion.abrirSesionYTransaccion();
            
            int idArticulo=MetodosComunes.devolverCodigoComboBox(comboArticulos);
            int idVendedor=MetodosComunes.devolverCodigoComboBox(comboVendedores);
            int idZona=MetodosComunes.devolverCodigoComboBox(comboZonas);
            
            ExVendedores vendedor=(ExVendedores)conexion.getSesion().load(ExVendedores.class, idVendedor);
            ExArticulos articulo=(ExArticulos)conexion.getSesion().load(ExArticulos.class, idArticulo);
            ExZonas zona=(ExZonas)conexion.getSesion().load(ExZonas.class, idZona);
            
            ExVentas ventas=new ExVentas();
            
            int idventa=conexion.proximoIDDisponible("idventa", "ExVentas");
            
            ventas.setIdventa(idventa);
            ventas.setFechaVenta(MetodosComunes.deFechaANumero(dateFechaVenta.getDate()));
            ventas.setUnidades(Short.parseShort(txtUnidades.getText()));
            
            ventas.setExArticulos(articulo);
            ventas.setExVendedores(vendedor);
            ventas.setExZonas(zona);
            
            ventas.setEliminado(false);
            
            conexion.insertar(ventas);
            
            conexion.cerrarSesionYTransaccion();
            
            JOptionPane.showMessageDialog(this, "Venta introducida");
            
        }else{
            JOptionPane.showMessageDialog(this, error);
        }
        
    }//GEN-LAST:event_btnAnadirActionPerformed

    
    
    
    
