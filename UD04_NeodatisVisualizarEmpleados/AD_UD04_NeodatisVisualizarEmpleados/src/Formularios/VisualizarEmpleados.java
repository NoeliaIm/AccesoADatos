    private void llenarDepartamentos() {
        String datos[] = new String[2];
        try {
            datos[0] = "-1";
            datos[1] = "Seleccionar";
            jCboDep.removeAllItems(); //Borra todos los Items
            jCboDep.addItem(new String[]{datos[0], datos[1],});
            Values valores = odb.getValues(new ValuesCriteriaQuery(Departamentos.class)
                    .field("deptNo")
                    .field("dnombre"));
            while (valores.hasNext()) {
                ObjectValues objectValues = (ObjectValues) valores.next();
                //Integer d= (Integer) objectValues.getByAlias("deptNo");;
                datos[0] = Integer.toString((Integer) objectValues.getByAlias("deptNo"));
                datos[1] = (String) objectValues.getByAlias("dnombre");
                jCboDep.addItem(new String[]{datos[0], datos[1],}); //Los aÃ±ade al Combo con dos columnas

            }

            jCboDep.setRenderer(new DefaultListCellRenderer() //Renderiza el Combo 
            {
                @Override
                public java.awt.Component getListCellRendererComponent(JList l, Object o, int i, boolean s, boolean f) {
                    return new JLabel(((String[]) o)[1]);  //Visualiza sÃ³lo la columna 1, que es el nombre
                }
            });


        } catch (Exception e) {
            System.out.println("error" + e);
        }


    }
     private void borrartabla(){
       int N=jTableEmpleados.getRowCount()-1;
       for (int i=N; i>=0;i--){
            DefaultTableModel temp = (DefaultTableModel) jTableEmpleados.getModel();
            temp.removeRow(i);
       }
    }
