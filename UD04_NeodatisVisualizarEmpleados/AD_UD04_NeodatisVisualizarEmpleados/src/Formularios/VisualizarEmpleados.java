    private void jCboDepActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jCboDepActionPerformed

        borrartabla();
        String[] Seleccion = (String[]) jCboDep.getSelectedItem(); // Recoge lo seleccionado
        if ("-1".equals(Seleccion[0]) && !inicio) {
            JOptionPane.showMessageDialog(null, "Debe seleccioar un departamento");
        } else {
            if (!inicio) {
                Integer Dept_no = Integer.parseInt(Seleccion[0]);
                Values valores = odb.getValues(new ValuesCriteriaQuery(Empleados.class,
                        new And().add(Where.equal("dept.deptNo", Dept_no)))
                        .field("empNo")
                        .field("apellido")
                        .field("salario"));
                if (!valores.hasNext()) {
                    JOptionPane.showMessageDialog(null, "El departamento " + Seleccion[1] + "no tiene empleados");
                    jCboDep.requestFocus();
                    jCboDep.setSelectedIndex(0); // Coloca la selección al principio
                } else {
                    while (valores.hasNext()) {
                        ObjectValues objectValues = (ObjectValues) valores.next();
                        DefaultTableModel t = (DefaultTableModel) jTableEmpleados.getModel();
                        Object nuevo[] = {objectValues.getByAlias("empNo"), objectValues.getByAlias("apellido"), objectValues.getByAlias("salario")};
                        t.addRow(nuevo);
                    }
                }
            } else {
                inicio = false;
            }
        }
    }//GEN-LAST:event_jCboDepActionPerformed

