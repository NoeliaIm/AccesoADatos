    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
       
        String error="";
        
        Object matriz[][]={ {txtDNI, txtNombreVend},
                            {"- DNI no debe estar vacio", "- Nombre no debe estar vacio"}
                          };
        error+=MetodosComunes.comprobarVacios(matriz);
        
        
        if(MetodosComunes.devolverCodigoComboBox(comboZonas)==-1){
            error="- La zona se debe seleccionar\n";
        }
        
        if(conexion.existeValor(txtDNI.getText().toUpperCase(), "EX_vendedores", "DNI") && codigoOperacion==UPDATE){
            error="- El DNI ya existe\n";
        }
        
        
        if(error.equals("")){
            
            int codigoZona=MetodosComunes.devolverCodigoComboBox(comboZonas);
            
            
            if(codigoOperacion==INSERT){
                
                int idDisponible=conexion.proximoIDDisponible("idvendedor", "ex_vendedores");
                
                
                
                conexion.ejecutarInstruccion("insert into ex_vendedores values("
                                                +idDisponible+", "
                                                + " '"+txtDNI.getText().toUpperCase()+"', "
                                                + " '"+txtNombreVend.getText()+"',"
                                                + " '"+txtDireccion.getText()+"', "
                                                + ""+txtTelefono.getText()+", "
                                                + ""+codigoZona+", "
                                                + "0)");
                
                conexion.commit();
                
                
            }else{
                
                
                
                conexion.ejecutarInstruccion("update ex_vendedores "
                                                +"set DNI='"+txtDNI.getText().toUpperCase()+"', "
                                                + ", nombre_vend='"+txtNombreVend.getText()+"', "
                                                + "direccion='"+txtDireccion.getText()+"' , "
                                                + "telefono="+txtTelefono.getText()+", "
                                                + "idzona="+codigoZona+""
                                                + " where idvendedor="+idVendedorActual+"");
                                                
                conexion.commit();
                
            }
            
            JOptionPane.showMessageDialog(this, "Vendedor Grabado");
            
            
        }else{
            JOptionPane.showMessageDialog(this, error);
        }
        
        
        
        
    }//GEN-LAST:event_btnGrabarActionPerformed

