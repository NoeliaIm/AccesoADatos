    private void txtDNIActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_txtDNIActionPerformed
       
        if(MetodosComunes.comprobarDNI(txtDNI.getText().toUpperCase())){
            
            

            if(conexion.existeValor(txtDNI.getText().toUpperCase(), "EX_VENDEDORES", "DNI")){
                
                if(conexion.devolverValorInt("eliminado", "ex_vendedores", "  DNI='"+txtDNI.getText().toUpperCase()+"'")==1){
                    JOptionPane.showMessageDialog(this, "El Vendedor esta eliminado");
                     txtDNI.requestFocus();
                }else{
                    
                    txtDNI.setEnabled(false);
                
                    txtDireccion.setEnabled(true);
                    txtNombreVend.setEnabled(true);
                    txtTelefono.setEnabled(true);

                    comboZonas.setEnabled(true);

                    btnGrabar.setEnabled(true);

                    btnNuevo.setEnabled(true);
                    
                    
                    btnEliminar.setEnabled(true);
                
                  codigoOperacion=UPDATE;
                    
                  rellenar("select idvendedor,nombre_vend, direccion, telefono, idzona from ex_vendedores where DNI='"+txtDNI.getText().toUpperCase()+"'");
                
                    
                }
                
                
            }else{
                
                codigoOperacion=INSERT;
                
            }
                
            
        }else{
            JOptionPane.showMessageDialog(this, "Error, el dni es incorrecto");
            txtDNI.requestFocus();
        }
        
        
        
        
    }//GEN-LAST:event_txtDNIActionPerformed

