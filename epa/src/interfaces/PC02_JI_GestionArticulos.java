    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
        
       int fila=TablaArticulos.getSelectedRow();

            if(fila!=-1){

                BigDecimal idArticulo=(BigDecimal)TablaArticulos.getValueAt(fila, 0);

                PC02_JD_Articulos articulos=new PC02_JD_Articulos(idArticulo.intValue());
                articulos.setVisible(true);
                
                rellenaTabla(CONSULTA_INICIAL);
                
            }else{
                JOptionPane.showMessageDialog(this, "No has selecccionado ninguna fila");
            }
        
        
        
        
    }//GEN-LAST:event_btnModificarActionPerformed

    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        
        int fila=TablaArticulos.getSelectedRow();

            if(fila!=-1){

                BigDecimal idArticulo=(BigDecimal)TablaArticulos.getValueAt(fila, 0);

                int seleccion=JOptionPane.showConfirmDialog(this, "¿Estas seguro de que quieres eliminar el articulo?");
                
                if(seleccion==JOptionPane.YES_OPTION){
                    
                    conexion.ejecutarInstruccion("update ex_articulos set eliminado=1 where idarticulo="+idArticulo.intValue()+"");
                    
                    conexion.commit();
                    
                    JOptionPane.showMessageDialog(this, "Articulo borrado");
                    
                    rellenaTabla(CONSULTA_INICIAL);
                    
                }
                
                
                
            }else{
                JOptionPane.showMessageDialog(this, "No has selecccionado ninguna fila");
            }
        
        
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

