    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed
       
        
        String condicion="";
        
        
        if(radioEliminado.isSelected()){
            condicion="eliminado=1";
        }else{
            condicion="eliminado=0";
        }
        
        
        String consulta="";
        
        if(txtDescr_Artic.getText().equals("")){
            consulta="select idarticulo, descr_artic, precio_unitario from ex_articulos where "+condicion+" order by descr_artic";
       
        }else{
            consulta="select idarticulo, descr_artic, precio_unitario from ex_articulos where "+condicion+" and upper(descr_artic) like '%"+txtDescr_Artic.getText().toUpperCase()+"%' order by descr_artic";
       
        }
        
        rellenaTabla(consulta);
        
        
    }//GEN-LAST:event_btnFiltrarActionPerformed

