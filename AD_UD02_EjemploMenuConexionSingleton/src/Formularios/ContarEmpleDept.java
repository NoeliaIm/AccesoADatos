    public static ContarEmpleDept getInstancia(Integer dept_no) {
        if (instancia != null) {
            instancia.dispose();
        }
        if (instancia == null || instancia.isClosed) {
            instancia = new ContarEmpleDept(dept_no);
        }
        return instancia;
    }

    private ContarEmpleDept(Integer dept_no) {
        try {
            initComponents();
            String sql = "SELECT COUNT(*) AS  TOTAL FROM EMPLEADOS E, DEPARTAMENTOS D "
                    + " WHERE E.IDDEPARTAMENTO=D.IDDEPARTAMENTO AND D.DEPT_NO=? ";
            try (PreparedStatement consulta = ConexionSingleton.conexion.prepareStatement(sql)) {
                consulta.setInt(1, dept_no);
                try (ResultSet resultado = consulta.executeQuery()) {
                    resultado.next();
                    int total = resultado.getInt(1);
                    Total.setText(Integer.toString(total));
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(ContarDepart.class.getName()).log(Level.SEVERE, null, ex);
        }

    }

