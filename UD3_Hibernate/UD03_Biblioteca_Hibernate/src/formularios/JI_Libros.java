     private void filtrar(){
        
         String sql = " FROM Blibros l ";
         
         if (radioEliminados.isSelected()) {
            sql = sql + " WHERE l.eliminado =1";
        } else {
            sql = sql + " WHERE l.eliminado =0";
        }
         if(txtISBN.getText().length() > 0){
             sql = sql + " and upper(l.isbn) like '%" + txtISBN.getText().toUpperCase() + "%'";
         }
         if(txtTitulo.getText().length() > 0){
             sql = sql + " and upper(l.titulo) like '%" + txtTitulo.getText().toUpperCase() + "%'";
         }
         if(txtEditorial.getText().length() > 0){
             sql = sql + " and upper(l.beditoriales.nombre) like '%" + txtEditorial.getText().toUpperCase() + "%'";
         }
         if(txtAutores.getText().length() > 0){
             sql = sql + " and upper(l.autores) like '%" + txtAutores.getText().toUpperCase() + "%'";
         }
         if(txtMateria.getText().length() > 0){
             sql = sql + " and upper(l.bmaterias.descmateria) like '%" + txtMateria.getText().toUpperCase() + "%'";
         }
          sql = sql + " order by l.titulo";
        
            llenarTablaLibrosHibernate(sql);
        
          
     }
     
     
      
      private void llenarTablaLibrosHibernate(String sql){
          limpiarTabla(tablaLibros);
          Session sesion = JF_Menu.instanciaHibernate.openSession();
          Query consulta = sesion.createQuery(sql);
          Iterator iter = consulta.iterate();
          while(iter.hasNext()){
              Blibros lib = (Blibros)iter.next();
              
              Object nuevo[] = {Integer.toString(lib.getIdlibro()),Long.toString(lib.getIsbn())
                                ,lib.getTitulo(),lib.getIdioma(),lib.getNrocopiastot(),lib.getBeditoriales().getNombre()
                                ,lib.getAutores(),lib.getBmaterias().getDescmateria()};
              tablaLibros.addRow(nuevo);
          }
          sesion.close();
          setOcultarColumnasJTable(jTableLibros,new int[]{0});
      }
      
      
    public static void setOcultarColumnasJTable(JTable tbl, int columna[]) {
        for (int i = 0; i < columna.length; i++) {
            tbl.getColumnModel().getColumn(columna[i]).setMaxWidth(0);
            tbl.getColumnModel().getColumn(columna[i]).setMinWidth(0);
            tbl.getTableHeader().getColumnModel().getColumn(columna[i]).setMaxWidth(0);
            tbl.getTableHeader().getColumnModel().getColumn(columna[i]).setMinWidth(0);
        }
    }
      
      private void limpiarTabla(DefaultTableModel tabla) {
        int a = tabla.getRowCount() - 1;  //Índices van de 0 a n-1
        for (int i = a; i >= 0; i--) {
            tabla.removeRow(i);
        }
    }

