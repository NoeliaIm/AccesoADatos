    public void eliminarHibernate(boolean eliminado, String texto) {
        if (jTableLibros.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "No hay libros");
        } else {

            if (jTableLibros.getSelectedRow() != -1) {
                
                //devuelve el valor de la fila selecciona y la columna 0
                int fila = jTableLibros.getSelectedRow();

               
                    int opcion = JOptionPane.showConfirmDialog(null, "¿Desea "+texto+" el registro : " + jTableLibros.getValueAt(fila, 2) + "?");
                    Session sesion = JF_Menu.instanciaHibernate.openSession();
                    if (opcion == JOptionPane.YES_OPTION) {
                        Transaction tx = sesion.beginTransaction();
                        Blibros lib = new Blibros();
                        lib = (Blibros)sesion.get(Blibros.class,Integer.parseInt(jTableLibros.getValueAt(fila,0).toString()));
                        lib.setEliminado(eliminado);
                        tx.commit();
                        System.out.println("Libro "+jTableLibros.getValueAt(fila, 0)+" borrado");
                        tablaLibros.removeRow(fila);
                        String texto1 = "";
                        if(texto.equals("eliminar")){
                            texto1 = "eliminado";
                        }else{
                            texto1 = "restaurado";
                        }
                        JOptionPane.showMessageDialog(null, "Registro "+texto1);

                    }
                    sesion.close();
                    
            } else {
                JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguno");
            }
        }
    }


