    private void eliminarHibernate(boolean eliminado, String texto) {
        if (jTableMultas.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "No hay multas");
        } else {
            if (jTableMultas.getSelectedRow() != -1) {
                int fila = jTableMultas.getSelectedRow();
                int opcion = JOptionPane.showConfirmDialog(null, "¿Desea " + texto + " el registro: " + jTableMultas.getValueAt(fila, 1) + "?");
                Session sesion = JF_Menu.instanciaHibernate.openSession();
                if (opcion == JOptionPane.YES_OPTION) {
                    Transaction tx = sesion.beginTransaction();
                    Bmultas mul = new Bmultas();
                    mul = (Bmultas) sesion.get(Bmultas.class, Integer.parseInt(jTableMultas.getValueAt(fila, 0).toString()));
                    mul.setEliminado(eliminado);
                    tx.commit();
                }
                tablaMultas.removeRow(fila);
                String texto1 = "";
                if (texto.equals("eliminar")) {
                    texto1 = "eliminado";
                } else {
                    texto1 = "restaurado";
                }
                JOptionPane.showMessageDialog(null, "Registro " + texto1);
                sesion.close();
            }

        }
    }

   

   


