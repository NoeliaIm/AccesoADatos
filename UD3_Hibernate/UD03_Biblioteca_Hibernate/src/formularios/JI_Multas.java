    private void llenarTablaMultasInicioHibernate() {
        String sql = "FROM Bmultas m WHERE m.eliminado=0 ORDER BY m.descmulta ";
        llenarTablaMultasHibernate(sql);
    }

    private void filtrar() {

        String sql = "FROM Bmultas m";

        if (radioEliminados.isSelected()) {
            sql = sql + " WHERE m.eliminado =1";
        } else {
            sql = sql + " WHERE m.eliminado =0";
        }
        if (txtDescripcion.getText().length() > 0) {
            sql = sql + " and upper(m.descmulta) like '%" + txtDescripcion.getText().toUpperCase() + "%'";
        }
        if (txtMinimo.getText().length() > 0) {
            sql = sql + " and upper(m.diasmin) like '%" + txtMinimo.getText().toUpperCase() + "%'";
        }
        if (txtMaximo.getText().length() > 0) {
            sql = sql + " and upper(m.diasmax) like '%" + txtMaximo.getText().toUpperCase() + "%'";
        }

        sql = sql + " order by m.descmulta";

        llenarTablaMultasHibernate(sql);

    }

    private void llenarTablaMultasHibernate(String sql) {
        limpiarTabla(tablaMultas);
        Session sesion = JF_Menu.instanciaHibernate.openSession();
        Query consulta = sesion.createQuery(sql);
        Iterator iter = consulta.iterate();
        while (iter.hasNext()) {
            Bmultas mul = (Bmultas) iter.next();
            Object nuevo[] = {mul.getIdmulta(), mul.getDescmulta(), mul.getDiasmin(), mul.getDiasmax(), mul.getCantidad()};
            tablaMultas.addRow(nuevo);
        }
        setOcultarColumnasJTable(jTableMultas, new int[]{0});
        sesion.close();
    }

  

    public static void setOcultarColumnasJTable(JTable tbl, int columna[]) {
        for (int i = 0; i < columna.length; i++) {
            tbl.getColumnModel().getColumn(columna[i]).setMaxWidth(0);
            tbl.getColumnModel().getColumn(columna[i]).setMinWidth(0);
            tbl.getTableHeader().getColumnModel().getColumn(columna[i]).setMaxWidth(0);
            tbl.getTableHeader().getColumnModel().getColumn(columna[i]).setMinWidth(0);
        }
    }

    private void limpiarTabla(DefaultTableModel tabla) {
        int a = tabla.getRowCount() - 1;  //Índices van de 0 a n-1
        for (int i = a; i >= 0; i--) {
            tabla.removeRow(i);
        }
    }

