    private void btnSalarioActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalarioActionPerformed
        
        
        if (conexion.consultaVacia(modelo)){
                JOptionPane.showMessageDialog(this, "No hay reultados, prueba con otro valor");
        }else{
            conexion.ejecutarConsulta("select apellido, salario from Empleados where salario=(select max(salario) from empleados where IDDEPARTAMENTO=(select IDDEPARTAMENTO from departamentos where DEPT_NO="+txtCodigoDep.getText()+"))");

            try {
                //Escribimos el nombre y apellido del empleado con mas salario
                conexion.getResultSet().next();
                txtNombreEmpleado.setText(conexion.getResultSet().getString("apellido"));

                txtSalarioEmpleado.setText(String.valueOf(conexion.getResultSet().getInt("salario")));

            } catch (SQLException ex) {
                Logger.getLogger(EmpleadosApp.class.getName()).log(Level.SEVERE, null, ex);
            }
        }
        
        
       
    }//GEN-LAST:event_btnSalarioActionPerformed

