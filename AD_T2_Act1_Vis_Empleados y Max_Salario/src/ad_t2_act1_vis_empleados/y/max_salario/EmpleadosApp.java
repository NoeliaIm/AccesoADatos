    private void btnVisualizarEmpleadosActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnVisualizarEmpleadosActionPerformed
        
        if (!conexion.existeValor(Integer.parseInt(txtCodigoDep.getText()), "DEPARTAMENTOS", "IDDEPARTAMENTO") ){
            JOptionPane.showMessageDialog(this, "Error, el departamento no existe");
        }else{
            
            //si la consulta obtiene 0 resultados nos lo indica
            if(conexion.consultaVaciaV2("select count(*) from Empleados e, departamentos d where e.IDDEPARTAMENTO=d.IDDEPARTAMENTO and IDDEPARTAMENTO="+txtCodigoDep.getText()+"")){
                 JOptionPane.showMessageDialog(this, "No hay resultados, prueba con otro valor");
            }else{
                
                //limpiamos la tabla para que no se acumulen los registros
                Limpiar.limpiarTabla(modelo);

                //ejecutamos la consulta y rellenamos la tabla
                conexion.ejecutarConsulta("select apellido,oficio,fecha_alt,salario  from Empleados e, departamentos d where e.IDDEPARTAMENTO=d.IDDEPARTAMENTO and IDDEPARTAMENTO="+txtCodigoDep.getText()+"");

                conexion.rellenaJTableBD(modelo);

                //Habilitamos y dehabilitamos controles
                txtCodigoDep.setEnabled(false);

                btnLimpiar.setEnabled(true);

                btnSalario.setEnabled(true);

                txtNombreEmpleado.setEnabled(true);

                txtSalarioEmpleado.setEnabled(true);
            }
            
            
            
        }
        
        
        
        
    }//GEN-LAST:event_btnVisualizarEmpleadosActionPerformed

