    public void cambiaAMayus(File f){
        
        try(FileReader fr=new FileReader(f)){
            
            int c=fr.read();
            while(c != -1){
                if(c>=97 && c<=122){
                  areaTexto.setText(areaTexto.getText()+ (char)(c-32));
                }else{
                    areaTexto.setText(areaTexto.getText()+ (char)c);
                }
                c=fr.read();
            }
            
        }catch(IOException e){
            
        }
        
    }
    
    
