    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        JFileChooser ventana=new JFileChooser();
        
        FileNameExtensionFilter filtro=new FileNameExtensionFilter("*.txt, *.ini, *.java", "txt", "java", "ini");
        ventana.setFileFilter(filtro);
        int seleccion=ventana.showOpenDialog(this.getContentPane());
        if (seleccion==JFileChooser.APPROVE_OPTION){
            directorioSeleccionado.setText(ventana.getSelectedFile().getAbsolutePath());
            cambiaAMayus(ventana.getSelectedFile());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

