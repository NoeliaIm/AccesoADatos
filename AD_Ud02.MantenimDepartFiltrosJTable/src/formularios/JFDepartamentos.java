    private void rellenarCampos() {
        try {
            if (!esNuevo) {
                String sql = "SELECT dept_no,dnombre,loc "
                        + "FROM DEPARTAMENTOS "
                        + "WHERE iddepartamento=" + idDepartamento;
                ResultSet resultado;
                try (Statement consulta = Conexion.conexion.createStatement()) {
                    resultado = consulta.executeQuery(sql);
                    resultado.next();
                    txtDeptNo.setText(resultado.getString("dept_no"));
                    txtDNombre.setText(resultado.getString("dnombre"));
                    txtLoc.setText(resultado.getString("loc"));
                }
            }
        } catch (SQLException ex) {
            Logger.getLogger(JFDepartamentos.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }

