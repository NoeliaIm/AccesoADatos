    private void txtDeptNoKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDeptNoKeyTyped
        if (txtDeptNo.getText().length() == 5) {
            evt.consume();
            txtMensaje.setText("Longitud Máxima: 5 caracteres");
        }
        if (!Character.isDigit(evt.getKeyChar())) {
            evt.consume();
            txtMensaje.setText("COD. DEPART: Sólo números");
        }
        if (evt.getKeyChar() == '\b') {
            txtMensaje.setText("");
        }
    }//GEN-LAST:event_txtDeptNoKeyTyped

    private void txtDNombreKeyTyped(java.awt.event.KeyEvent evt) {//GEN-FIRST:event_txtDNombreKeyTyped

        if (txtDNombre.getText().length() == 50) {
            evt.consume();
            txtMensaje.setText("Longitud Máxima: 50 caracteres");
        }
        if (!Character.isSpaceChar(evt.getKeyChar()) && !Character.isAlphabetic(evt.getKeyChar())) {
            evt.consume();
            txtMensaje.setText("NOMBRE: Sólo caracteres alfabéticos");
        }
        if (evt.getKeyChar() == '\b') {
            txtMensaje.setText("");
        }

    }//GEN-LAST:event_txtDNombreKeyTyped

