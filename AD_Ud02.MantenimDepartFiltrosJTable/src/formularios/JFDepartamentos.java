    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        try {
            String sql;
            Statement consulta = bd.conexion.createStatement();
            if (!esNuevo) {
                sql = "UPDATE DEPARTAMENTOS  SET  dept_no=" + txtDeptNo.getText()
                        + ", dnombre='" + txtDNombre.getText()
                        + "', loc='" + txtLoc.getText()
                        + "'  WHERE iddepartamento=" + idDepartamento;
                int filas = consulta.executeUpdate(sql);
                consulta.close();
                salir();
            } else {
                Integer idDep = maximo("DEPARTAMENTOS", "IDDEPARTAMENTO") + 1;
                sql = "INSERT INTO DEPARTAMENTOS VALUES (" + idDep + "," + txtDeptNo.getText()
                        + ",'" + txtDNombre.getText() + "','" + txtLoc.getText() + "',0)";

                int filas = consulta.executeUpdate(sql);
                consulta.close();
                salir();
            }
        } catch (SQLException ex) {
            Logger.getLogger(JFDepartamentos.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnGrabarActionPerformed
