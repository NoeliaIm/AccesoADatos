    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
        // TODO add your handling code here:
        if (jTableDepart.getRowCount() == 0) {
            JOptionPane.showMessageDialog(null, "No hay departamentos");
        } else {
            if (jTableDepart.getSelectedRow() != -1) {
                //devuelve el valor de la fila selecciona y la columna 0
                int fila = jTableDepart.getSelectedRow();
                int opcion = JOptionPane.showConfirmDialog(null, "Desea eliminar el registro : " + jTableDepart.getValueAt(fila, 2));
                if (opcion == JOptionPane.YES_OPTION) {
                    try {
                        String sql = "update departamentos set eliminado=1 where IdDepartamento=" + jTableDepart.getValueAt(fila, 0);
                        ResultSet filas;
                        try (Statement consulta = bd.conexion.createStatement()) {
                            filas = consulta.executeQuery(sql);
                            tablaDepart.removeRow(fila);
                        }
                        filas.close();
                    } catch (SQLException ex) {
                        Logger.getLogger(MantenimientoDepartamentos.class.getName()).log(Level.SEVERE, null, ex);
                    }
                }
            } else {
                JOptionPane.showMessageDialog(null, "No se ha seleccionado ninguno");
            }
        }

    }//GEN-LAST:event_btnEliminarActionPerformed

