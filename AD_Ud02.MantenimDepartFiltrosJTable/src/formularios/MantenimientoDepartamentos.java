    private void btnFiltrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnFiltrarActionPerformed

        try {
            String sql = "SELECT IdDepartamento,DEPT_NO,DNOMBRE,LOC FROM DEPARTAMENTOS";
            if (radioEliminados.isSelected()) {
                sql = sql + " where eliminado =1";
            } else {
                sql = sql + " where eliminado =0";
            }
            if (txtDNombre.getText().length() > 0) {
                sql = sql + " and dnombre like " + "'%" + txtDNombre.getText().toUpperCase() + "%'";
            }
            if (txtLoc.getText().length() > 0) {
                sql = sql + " and loc like '%" + txtLoc.getText().toUpperCase() + "%'";
            }
            sql=sql+" order by dept_no";
            llenartablaDepartamentos(sql);
        } catch (SQLException ex) {
            Logger.getLogger(MantenimientoDepartamentos.class
                    .getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_btnFiltrarActionPerformed

