    public void meterFicheroXML(){
        
        try(ObjectInputStream ois=new ObjectInputStream(new FileInputStream(FICHERO))){
            
            while(true){
                
                Peliculas p=(Peliculas)ois.readObject();
                
                int id=p.getIdPelicula();
                String titulo=p.getTitulo();
                int anio=p.getAnioProduccion();
                String director=p.getDirector();
                String nacionalidad=p.getNacionalidad();
                int presupuesto=p.getPresupuesto();
                char tipo=p.getTipoPelicula();
                
                Element raiz=xml.getDocumento().createElement("pelicula");
                xml.getDocumento().getDocumentElement().appendChild(raiz);
                
               

                xml.CrearElemento("id", Integer.toString(id), raiz);

                xml.CrearElemento("titulo", titulo, raiz);

                xml.CrearElemento("añoproduccion", Integer.toString(anio), raiz);

                xml.CrearElemento("director", director, raiz);
                
                xml.CrearElemento("nacionalidad", nacionalidad, raiz);
                
                xml.CrearElemento("presupuesto", Integer.toString(presupuesto), raiz);
                
                xml.CrearElemento("tipo", String.valueOf(tipo), raiz);

                

                
            }
            
            
            
        }catch(EOFException e){
            
        }catch(IOException e){
            
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Ejercicio3_CrearPeliculasXML_PC02.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    public void leerFicheroXML(){
        
        
        //Nos devuelve la lista de nodos
        NodeList peliculas=xml.listaNodos("pelicula");
        
        //Limpio el area de texto antes de volver a escribir
        txtArea.setText("");
        
        //Escribimos en el area de texto
        for(int i=0;i<peliculas.getLength();i++){
            
            Node articulo=peliculas.item(i);
            if(articulo.getNodeType()==Node.ELEMENT_NODE){
                Element elemento=(Element) articulo;

                
                
                
                txtArea.append( "Pelicula "+i+"\n"+
                                "ID: "+xml.getNodo("id", elemento)+"\n"+
                                "Titulo: "+xml.getNodo("titulo", elemento)+"\n"+
                                "Año: "+xml.getNodo("añoproduccion", elemento)+"\n"+
                                "Director: "+xml.getNodo("director", elemento)+"\n"+
                                "Nacionalidad: "+xml.getNodo("nacionalidad", elemento)+"\n"+
                                "Presupuesto: "+xml.getNodo("presupuesto", elemento)+"\n"+
                                "Tipo : "+xml.getNodo("tipo", elemento)+"\n\n");

            }
        }
        
    }
    
    
    
    
