    public  String devolverRutaFichero(String ruta){
       
        
        int pos=ruta.lastIndexOf(File.separator);

        if(pos!=-1){
            return ruta.substring(0, pos+1);
        }else{
            return ruta;
        }
        
    }
    
    public void soloNumerosYCaracteres(){
        
        
        ficheroCopia=new File(devolverRutaFichero(fichero.getAbsolutePath())+"COPIA-"+fichero.getName());
        
        System.out.print(ficheroCopia.getAbsolutePath());
        
        try(FileReader fr=new FileReader(fichero);
                FileWriter fw=new FileWriter(ficheroCopia)){
            
            
            int caracter=fr.read();
            
            while(caracter!=-1){
                if(Character.isAlphabetic(caracter) && caracter!=241){
                    fw.write(caracter);
                }
                caracter=fr.read();
            }
            
            
            
        }catch(FileNotFoundException e){
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }catch(IOException e){
            JOptionPane.showMessageDialog(null, e.getMessage(), "Error", JOptionPane.ERROR_MESSAGE);
        }
    }
