    private void btnElegirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnElegirActionPerformed
       
        JFileChooser ventana=new JFileChooser();
        FileNameExtensionFilter filtro=new FileNameExtensionFilter("*.txt", "txt");
        ventana.setFileFilter(filtro);
        int seleccion=ventana.showOpenDialog(this);
        if(seleccion==JFileChooser.APPROVE_OPTION){
            
            txtFichero.setText(ventana.getSelectedFile().toString());
            MetodosComunes.deFicheroATextArea(txtArea1, ventana.getSelectedFile());
            fichero=new File(ventana.getSelectedFile().toString());
        }
        
      
    }//GEN-LAST:event_btnElegirActionPerformed

    private void btnBuscarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBuscarActionPerformed
        
        if(fichero==null){
            JOptionPane.showMessageDialog(this, "debes seleccionar un fichero", "Error", JOptionPane.ERROR_MESSAGE);
        }else{
            if(txtCaracter.getText().isEmpty()){
                JOptionPane.showMessageDialog(this, "No puede estar vacio", "Error", JOptionPane.ERROR_MESSAGE);
            
            }else{
                if(txtCaracter.getText().length()>1 ){
                    JOptionPane.showMessageDialog(this, "Solo puede haber un caracter", "Error", JOptionPane.ERROR_MESSAGE);
                }else{

                    String contenidoFichero=MetodosComunes.leeFicheroCaracteresBuf(fichero);
                    int numeroVeces=MetodosComunes.numeroCaracteres(contenidoFichero, txtCaracter.getText().charAt(0));

                    txtVeces.setText(String.valueOf(numeroVeces));

                }
            }
            
        }
        
        
    }//GEN-LAST:event_btnBuscarActionPerformed

