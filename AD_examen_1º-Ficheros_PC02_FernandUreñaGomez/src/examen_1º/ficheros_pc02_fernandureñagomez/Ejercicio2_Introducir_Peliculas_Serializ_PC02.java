    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed
        
        //Matriz con los campos y textos personalizados de error
        Object matriz[][]={
                                 {txtID, txtTitulo, txtDirector},
                                 {" El ID no puede estar vacio\n", 
                                    " El titulo no puede estar vacio\n",
                                        " El director no puede estar nulo\n"}
                                   
                                };
        
        
        String error=MetodosComunes.comprobarVacios(matriz);
        
        if(cmbTipoPelicula.getSelectedIndex()==0){
            error+=" Debes elegir un tipo de pelicula";
        }
        
        if(error.equals("")){
            
            char caracterPeli=cmbTipoPelicula.getSelectedItem().toString().charAt(0);
            
           
            int anio=0;
            int presupuesto=0;
            
            if(!txtAnio.getText().isEmpty()){
                anio=Integer.parseInt(txtAnio.getText());
            }
            
            if(!txtPresupuesto.getText().isEmpty()){
                presupuesto=Integer.parseInt(txtAnio.getText());
            }
            
            Peliculas peli=new Peliculas(Integer.parseInt(txtID.getText()),
                                            txtTitulo.getText(),
                                            anio,
                                            txtDirector.getText(),
                                            txtNacionalidad.getText(),
                                            presupuesto,
                                            caracterPeli);
            
            
            MetodosComunes.escribirFicheroSerializado(ficheroObjetos, peli);
            
            JOptionPane.showMessageDialog(this, "Registro Grabado");
            
            MetodosComunes.limpiaCampos(campos);
            
            cmbTipoPelicula.setSelectedIndex(0);
            
        }else{
            JOptionPane.showMessageDialog(this, error, "errores", JOptionPane.ERROR_MESSAGE);
        }
            
        
    }//GEN-LAST:event_btnGrabarActionPerformed

