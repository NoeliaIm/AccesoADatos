    private void txtIDFocusLost(java.awt.event.FocusEvent evt) {//GEN-FIRST:event_txtIDFocusLost
        if(comprobarObjetoExistente(ficheroObjetos, Integer.parseInt(txtID.getText()))){
            JOptionPane.showMessageDialog(this, "La pelicula ya existe", "errores", JOptionPane.ERROR_MESSAGE);
            txtID.requestFocus();
            txtID.setText("");
        }
        
        if(txtID.getText().isEmpty()){
            JOptionPane.showMessageDialog(this, "Debes escribir un id", "errores", JOptionPane.ERROR_MESSAGE);
            txtID.requestFocus();
            txtID.setText("");
        }
        
    }//GEN-LAST:event_txtIDFocusLost


    
    public static boolean comprobarObjetoExistente(File fichero, int id){
        
        boolean existe=false;
        
        if(fichero.exists()){
            try(ObjectInputStream ois=new ObjectInputStream(new FileInputStream(fichero))){
               
            while(true){
                Peliculas peliActual=(Peliculas) ois.readObject();
                
                if(id==peliActual.getIdPelicula()){
                    existe=true;
                }
            }
           
            }catch(ClassNotFoundException e){
                System.out.println("Clase no encontrada");
            }catch(EOFException e){
                  
            }catch(IOException e){
                  System.out.println(e.getMessage());
            }
        }
        
        return existe;
        
    }
          
    
    
    
