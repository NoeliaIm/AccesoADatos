    private void rellenaDatos(int idSubParte){
    
        conexion.ejecutarConsulta("select * from s_subpartes where idSubparte="+idSubParte);
        
        ResultSet aux=conexion.getResultSet();
        
        try {
        
            aux.next();
        
            String tipo=aux.getString("tipo");
            
            if(tipo.equals("P")){
                cmbTipo.setSelectedIndex(1);
            }else{
                cmbTipo.setSelectedIndex(2);
            }
            
            txtAreaDescripcion.setText(aux.getString("descrip"));
            
            txtCoste.setText(String.valueOf(aux.getDouble("coste")));
            
            
        } catch (SQLException ex) {
            Logger.getLogger(InformacionSubParte.class.getName()).log(Level.SEVERE, null, ex);
        }
        
    }
    
    
