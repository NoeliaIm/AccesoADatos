    private void btnGrabarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnGrabarActionPerformed

        String error="";

        if(cmbTipo.getSelectedIndex()==0){
            error+="- Indica el tipo\n";
        }

        if(txtCoste.getText().isEmpty()){
            error+="- Escribe un coste\n";
        }

        //Si no hay errores, la operacion se puede completar
        if(error.equals("")){

            //Sacamos algunos datos necesarios

            char tipo=cmbTipo.getSelectedItem().toString().charAt(0);

            //Depende del codigo de operacion, haremos una operacion u otra
            if (codigoOperacion==frmPrincipal.INSERT){

                idSubParteActual=conexion.proximoIDDisponible("idsubparte", "s_subpartes");

                conexion.ejecutarInstruccion("insert into s_subpartes values("
                    + idSubParteActual + ", "
                    + "'"+tipo+"', "
                    + "'"+txtAreaDescripcion.getText()+"', "
                    + txtCoste.getText()+", "
                    + idParteActual+ ", "
                    + "0)");
                
                JOptionPane.showMessageDialog(this, "Subparte a√±adida");

            }else{

                conexion.ejecutarInstruccion("update s_subpartes "
                    + "set tipo='" + tipo + "', "
                    + "descrip='"+txtAreaDescripcion.getText()+ "', "
                    + "coste="+txtCoste.getText()
                    + " where idsubparte="+idSubParteActual);

                JOptionPane.showMessageDialog(this, "Subparte actualizada");
            }

            conexion.commit();
            
            this.setVisible(false);

        }else{
            JOptionPane.showMessageDialog(this, error, "Error", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_btnGrabarActionPerformed

