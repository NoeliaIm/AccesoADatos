    private void btnEliminarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEliminarActionPerformed
       
        if (jTable1.getRowCount()==0){
            
            JOptionPane.showMessageDialog(this, "No hay registros que eliminar");
        
        }else{
        
            int fila=jTable1.getSelectedRow();

            if(fila!=-1){

                BigDecimal idModelo=(BigDecimal)jTable1.getValueAt(fila, 0);
                
                String sql="select count(*) "
                        + "from s_vehiculos "
                        + "where eliminado=0 "
                        + "and "
                        + "idmodelo="+idModelo.intValue();
                
                //Compruebo que puedo borrar sin problemas (no hay modelos activos en vehiculos)
                if (!conexion.consultaVaciaV2(sql)){
                    
                    JOptionPane.showMessageDialog(this, 
                                                "No se puede eliminar el modelo, existen modelos en vehiculos.", 
                                                "Error", 
                                                JOptionPane.ERROR_MESSAGE);
                    
                }else{
                    
                    int seleccion=JOptionPane.showConfirmDialog(this, 
                                                            "Â¿Estas seguro de que quieres eliminar el registro?", 
                                                            "Confirmacion", 
                                                            JOptionPane.YES_NO_OPTION);
            
                    if(seleccion==JOptionPane.YES_OPTION){

                        //Actualizamos eliminado
                        conexion.ejecutarInstruccion("update s_modelos "
                                                        + "set eliminado=1"
                                                        + "where idModelo="+idModelo);

                        conexion.commit();
                        
                        //Confirmacion
                        JOptionPane.showMessageDialog(this, 
                                                    "Modelo eliminado", 
                                                    "Exito", 
                                                    JOptionPane.INFORMATION_MESSAGE);

                    }

                }

                rellenaModelos(CONSULTAMODELOS+devolverEstado()+devolverMarca());
                
            }else{
                JOptionPane.showMessageDialog(this, "No has selecccionado ninguna fila");
            }
        }
        
        
    }//GEN-LAST:event_btnEliminarActionPerformed

