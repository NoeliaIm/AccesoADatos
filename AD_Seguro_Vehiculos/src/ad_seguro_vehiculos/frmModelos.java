    private void btnModificarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnModificarActionPerformed
       
        if (jTable1.getRowCount()==0){
            
            JOptionPane.showMessageDialog(this, "No hay registros que modificar");
        
        }else{
        
            int fila=jTable1.getSelectedRow();

            if(fila!=-1){
                
                BigDecimal idModelo=(BigDecimal)jTable1.getValueAt(fila, 0);    
                
                InformacionModelo ventana=new InformacionModelo(idModelo.intValue());
        
                ventana.setVisible(true);
                
                rellenaModelos(CONSULTAMODELOS+devolverEstado()+devolverMarca());
                
            }else{
                JOptionPane.showMessageDialog(this, "No has selecccionado ninguna fila");
            }
        }
        
    }//GEN-LAST:event_btnModificarActionPerformed

