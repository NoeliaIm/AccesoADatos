    private void rdbEliminadoItemStateChanged(java.awt.event.ItemEvent evt) {//GEN-FIRST:event_rdbEliminadoItemStateChanged
       
        //Al pulsar en Eliminado o no eliminado, se rellenan sus respectivos datos
        
        if (ItemEvent.SELECTED==evt.getStateChange()){
            
            btnModificar.setEnabled(false);
            btnEliminar.setEnabled(false);
            
            rellenaModelos(CONSULTAMODELOS+ELIMINADO+devolverMarca());
            
        }else{
            
            btnEliminar.setEnabled(true);
            btnModificar.setEnabled(true);
            
            rellenaModelos(CONSULTAMODELOS+NOELIMINADO+devolverMarca());
            
        }
        
        
    }//GEN-LAST:event_rdbEliminadoItemStateChanged

