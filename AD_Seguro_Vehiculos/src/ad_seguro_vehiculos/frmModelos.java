    private void rellenaModelos(String consulta){
        
        conexion.ejecutarConsulta(consulta);
        
        MetodosComunes.limpiarTabla(modelo);
        
        conexion.rellenaJTableBD(modelo);
        
        //Oculto el id de modelo
        MetodosComunes.ocultarColumnaJTable(jTable1, 0);
        
        //Centro los datos y cabecera
        MetodosComunes.centraCabecera(jTable1);
        MetodosComunes.centrarDatos(jTable1);
        
       
        
        conexion.cerrarResult();
        conexion.cerrarSentencia();
        
    }
    
    //Indica si mostrar eliminados o no eliminados
    private String devolverEstado(){
        
        if (rdbEliminado.isSelected()){
            return ELIMINADO;
        }else{
            return NOELIMINADO;
        }
        
    }
    
    //Indica que mara devuelve (si hay alguna seleccionada)
    private String devolverMarca(){
        
        String condicion="";

        if(MetodosComunes.devolverCodigoComboBox(cmbMarca)!=-1){
            condicion+=" and ma.idmarca="+MetodosComunes.devolverCodigoComboBox(cmbMarca)+"";
        }
        
        return condicion;
        
    }
    
